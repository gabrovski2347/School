public class Car
{
    private String color;
    private double speed;

    public Car(String color, double speed)
    {
        this.color = color;
        this.speed = speed;
    }

    public String getColor()
    {
        return color;
    }

    public void setColor(String color)
    {
        this.color = color;
    }

    public double getSpeed() {
        return speed;
    }

    public void setSpeed(double speed) {
        this.speed = speed;
    }
}
